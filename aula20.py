primeiro_valor = input('Digite um valor: ')
segundo_valor = input('Digite um valor: ')

if primeiro_valor > segundo_valor :
    print(f'Primeiro valor {primeiro_valor} é maior que o segundo valor {segundo_valor}')
elif primeiro_valor == segundo_valor :
    print('Os valores informados são iguais')
else :
    print(f'Segundo valor {segundo_valor} é maior que o segundo valor {primeiro_valor}')