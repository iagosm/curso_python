#Tipos de int e float
# int-> Número Inteiro
# o tipo int Representa qualquer número
#positivo ou negativo. int sem sinal é considerado positivo
print(11) # int
print(-11) # int
print(0)

#Float -> numero com pontos flutuantes
#o tipo float representa quaqluer número
# o positivo ou negativo com ponto flutuante.
#float sem sinal é considerado positivo.
print(1.1, 10.11, sep=" - ") # float
print(0.0, -1.5)

#a função type mostra o tipo que o python
#inferiu ao valor
print(type('Otávio'))
print(type(1))
print(type(-1.2), type(150))